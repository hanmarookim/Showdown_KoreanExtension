// REGEX
var regex_forfeited = new RegExp(/(The opposing )*(.+) forfeited./);
var regex_battlecount = new RegExp(/(^[0-9])  (battle|battles)/);
var regex_valid = new RegExp(/Your team is valid for (.+)./);
var regex_top100 = new RegExp(/(.+) Top 100/);
//var regex_rejected = new RegExp(/Your team was rejected for the following reasons: -(.+)/);
var regex_joined = new RegExp(/You joined (.+)/);
var regex_inviteonly = new RegExp(/The room "(.+)" is invite-only, and you haven't been invited./);
var regex_battletimer = new RegExp(/Battle timer is ON: inactive players will automatically lose when time's up. \(requested by (.+)\)/);
var regex_battletimer1 = new RegExp(/(.+) has ([0-9\.]+) seconds left./);
var regex_notfound = new RegExp(/No Pokemon, item, move, ability or nature named \'(.+)\' was found. \(Check your spelling\?\)/);
var regex_instead = new RegExp(/No Pokemon, item, move, ability or nature named \'(.+)\' was found. Showing the data of \'(.+)\' instead./);
var regex_promoted = new RegExp(/You were promoted to (.+) by (.+)./);
var regex_demoted = new RegExp(/You were demoted to (.+) by (.+)/);
var regex_promotedchat = new RegExp(/(.+) was promoted to (.+) by (.+)./);
var regex_demotedchat = new RegExp(/\((.+) was demoted to (.+) by (.+).\)/);
var regex_formatnotfound = new RegExp(/No (.+) battles are going on right now./);
var regex_vs = new RegExp(/(.+) vs. (.+)/);

var t = function(originalStr) {
    var tmp = originalStr.trim();
    if (translations[tmp])
        return translations[tmp];

    if(originalStr.match(regex_forfeited)){ // TRANSLATE MATCH REGEX
        return dx(RegExp.$1) + RegExp.$2 + "이(가) 항복했습니다.";
    }
    if(originalStr.match(regex_battlecount)){
        return "배틀 " + RegExp.$1 + "판 진행 중";
    }
    if(originalStr.match(regex_valid)){
        return "이 파티의 " + dx(RegExp.$1) + " 룰에 대한 적법성을 검사했습니다.";
    }
    if(originalStr.match(regex_top100)){
        return "" + dx(RegExp.$1) + " Top 100";
    }
    if(originalStr.match(regex_joined)){
        return RegExp.$1 + "에 접속했습니다";
    }
    if(originalStr.match(regex_inviteonly)){
        return "초대받은 플레이어만 접속할 수 있습니다.";
    }
    if(originalStr.match(regex_battletimer)){
        return "배틀 타이머 ON: 응답이 없는 플레이어는 타이머 종료 시 자동으로 패배합니다. (" + RegExp.$1 + "의 요청)";
    }
    if(originalStr.match(regex_battletimer1)){
        return RegExp.$1 + ": " + RegExp.$2 + "초 남음";
    }
    if(originalStr.match(regex_notfound)){
        return "'" + RegExp.$1 + "'에 해당하는 포켓몬, 도구, 기술, 특성, 성격이 없습니다. (맞춤법을 확인하셨습니까?)";
	}
	if(originalStr.match(regex_instead)){
        return "'" + RegExp.$1 + "'에 해당하는 포켓몬, 도구, 기술, 특성, 성격이 없습니다. 대신 유사한 '" + dx(RegExp.$2) + "'의 데이터를 열람합니다.";
	}
	if(originalStr.match(regex_promoted)){
        return RegExp.$2 + "에 의해 '" + dx(RegExp.$1) + "' 랭크로 승급했습니다.";
	}
	if(originalStr.match(regex_demoted)){
        return RegExp.$2 + "에 의해 '" + dx(RegExp.$1) + "' 랭크로 강등했습니다.";
	}
	if(originalStr.match(regex_promotedchat)){
        return RegExp.$3 + "이(가) " + RegExp.$1 + "을(를) " + dx(RegExp.$2) + " 랭크로 승급했습니다.";
	}
	if(originalStr.match(regex_demotedchat)){
        return "(" + RegExp.$3 + "이(가) " + RegExp.$1 + "을(를) " + dx(RegExp.$2) + " 랭크로 강등했습니다.)";
	}
	if(originalStr.match(regex_formatnotfound)){
        return "진행 중인 " + dx(RegExp.$1) + " 배틀이 없습니다.";
	}
	if(originalStr.match(regex_vs)){
        return RegExp.$1 + " vs. " + RegExp.$2;
	}
    /* if(originalStr.match(regex_rejected)){
        return "파티가 다음 이유로 인해 적법성을 통과하지 못했습니다:" + RegExp.$1;
    }*/

    return originalStr.replace("", "");

}

function translateElement(element) {
    var elTW = document.createTreeWalker(element, NodeFilter.SHOW_Element, null, false);
    var node = null;
    var translate = t;
    while ((node = elTW.nextNode()) != null) {
        if (node.nodeValue) {

            var value = node.nodeValue;
            if (value.indexOf('·') != -1) {
                value = value.replace('·', "");
                value = translate(value);
                node.nodeValue = "· " + value;
                continue;
            } else
                node.nodeValue = translate(node.nodeValue.replace("é", "e")

                );
        }
    }
}

(function() {
    'use strict';
    if (document.getElementById('room-'))
        translateElement(document.getElementById('room-'));
    $(document).on('DOMNodeInserted', function(e) {
        translateElement(e.target);
    });
})();
